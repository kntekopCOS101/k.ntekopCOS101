fn main() {
    // addition
    let sum = 5550 + 7310;
    println!("the sum of 5550 and 7310 is {}",sum);

    // subtraction
    let difference = 5550 - 7310;
    println!("the difference of 5550 and 7310 is {}",difference);


    // multiplication
    let product:f32 = 4.0 * 30.0;
    println!("the product of 4 and 30 is {}",product);


    // division
    let quotient = 56.7 / 32.2;
    println!("the quotient of 56.7 and 32.2 is {}",quotient);


    // remainder
    let remainder = 43 % 5;
    println!("the remainder of 43 and 5 is {}",remainder);
}
